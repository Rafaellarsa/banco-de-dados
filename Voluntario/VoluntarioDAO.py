import traceback
import psycopg2
from Voluntario.Voluntario import Voluntario
import PsycopgParameters

class VoluntarioDAO(object):
    def listar_todos(self):
        resultados = []
        try:
            connection = psycopg2.connect(user=PsycopgParameters.user, password=PsycopgParameters.password,
                                          host=PsycopgParameters.host, port=PsycopgParameters.port,
                                          database=PsycopgParameters.database)
            cursor = connection.cursor()
            cursor.execute("SELECT id_usuario, data_inicio FROM public.\"Voluntario\"")
            registros = cursor.fetchall()
            for r in registros:
                v = Voluntario()
                v.id_usuario = r[0]
                v.data_inicio = r[1]
                resultados.append(v)
        except (Exception, psycopg2.Error) as error:
            traceback.print_exc()
        finally:
            if connection:
                cursor.close()
                connection.close()
        return resultados

    def listar(self, id_usuario):
        v = None
        try:
            connection = psycopg2.connect(user=PsycopgParameters.user, password=PsycopgParameters.password,
                                          host=PsycopgParameters.host, port=PsycopgParameters.port,
                                          database=PsycopgParameters.database)
            cursor = connection.cursor()
            cursor.execute("SELECT id_usuario, data_inicio FROM public.\"Voluntario\" WHERE id_usuario = " + str(id_usuario))
            r = cursor.fetchone();
            if cursor.rowcount == 1:
                v = Voluntario()
                v.id_usuario = r[0]
                v.data_inicio = r[1]
        except (Exception, psycopg2.Error) as error:
            traceback.print_exc()
        finally:
            if connection:
                cursor.close()
                connection.close()
        return v

